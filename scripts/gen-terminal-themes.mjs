import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { join } from "node:path";

// Transcodes the mbadolato/iTerm2-Color-Schemes (MIT) "windowsterminal" schemes
// into xterm.js ITheme objects. Reads from the vendored submodule by default;
// pass a path to a `windowsterminal` directory to override. Run via
// `pnpm generate` (after `git submodule update --init`).

const srcDir =
  process.argv[2] ??
  fileURLToPath(new URL("../vendor/iterm2-color-schemes/windowsterminal", import.meta.url));

// Windows Terminal scheme key -> xterm ITheme key. WT names the magenta slot
// "purple"; everything else lines up one-to-one.
const ANSI = {
  black: "black",
  red: "red",
  green: "green",
  yellow: "yellow",
  blue: "blue",
  purple: "magenta",
  cyan: "cyan",
  white: "white",
  brightBlack: "brightBlack",
  brightRed: "brightRed",
  brightGreen: "brightGreen",
  brightYellow: "brightYellow",
  brightBlue: "brightBlue",
  brightPurple: "brightMagenta",
  brightCyan: "brightCyan",
  brightWhite: "brightWhite",
};

function isHex(value) {
  return typeof value === "string" && /^#[0-9a-fA-F]{6}$/.test(value);
}

// Perceived brightness of the background; matches the dark/light split used by
// the original sing-box-for-apple Ghostty integration.
function isDark(hex) {
  const h = hex.slice(1);
  const r = parseInt(h.slice(0, 2), 16);
  const g = parseInt(h.slice(2, 4), 16);
  const b = parseInt(h.slice(4, 6), 16);
  return 0.299 * r + 0.587 * g + 0.114 * b < 128;
}

let files;
try {
  files = readdirSync(srcDir);
} catch {
  console.error(`cannot read ${srcDir}\nrun \`git submodule update --init\` first.`);
  process.exit(1);
}

const entries = [];
for (const file of files) {
  if (!file.endsWith(".json")) continue;
  const raw = JSON.parse(readFileSync(join(srcDir, file), "utf8"));
  if (!isHex(raw.background) || !isHex(raw.foreground)) continue;
  const theme = { background: raw.background, foreground: raw.foreground };
  if (isHex(raw.cursorColor)) theme.cursor = raw.cursorColor;
  if (isHex(raw.selectionBackground)) theme.selectionBackground = raw.selectionBackground;
  for (const [wtKey, xtermKey] of Object.entries(ANSI)) {
    if (isHex(raw[wtKey])) theme[xtermKey] = raw[wtKey];
  }
  entries.push({
    name: raw.name ?? file.replace(/\.json$/, ""),
    isDark: isDark(raw.background),
    theme,
  });
}

entries.sort((a, b) => a.name.localeCompare(b.name, "en"));

const out = [
  "// Generated by scripts/gen-terminal-themes.mjs — do not edit by hand.",
  "// Source: mbadolato/iTerm2-Color-Schemes (MIT), windowsterminal schemes,",
  "// transcoded to xterm.js ITheme objects.",
  'import type { TerminalThemeEntry } from "./terminalTheme";',
  "",
  "export const GENERATED_TERMINAL_THEMES: TerminalThemeEntry[] = [",
  ...entries.map((entry) => `  ${JSON.stringify(entry)},`),
  "];",
  "",
];
writeFileSync(new URL("../src/lib/terminalThemes.generated.ts", import.meta.url), out.join("\n"));
console.log(`wrote ${entries.length} themes`);
